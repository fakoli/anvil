"""Synthesise a draft ``prd.md`` from a scanned :class:`CodebaseModel`.

The brownfield scan cannot invent product intent, so the draft PRD it writes is
explicitly a *seed*: it summarises what the repo already contains (components,
languages, file counts) and emits one feature + one task per non-trivial
top-level component. Each task carries placeholder acceptance-criteria and a
verification command so the existing parse → plan → score → review pipeline can
promote it to ``ready`` offline — giving the user a concrete, claimable starting
point they then edit to reflect real intent.

The output is deliberately formatted to match exactly what
``planning.template.parse_prd`` accepts (``## Features`` / ``## Tasks`` blocks,
``**Feature:**`` / ``**Acceptance criteria:**`` / ``**Verification:**`` fields)
so the draft is round-trippable through the engine without manual fixups.
"""

from __future__ import annotations

from anvil.scan.model import CodebaseModel

__all__ = ["draft_prd_from_model"]

# Components with fewer than this many files do not warrant their own
# feature/task — they are rolled into the summary only. Keeps the draft graph
# focused on the substantive parts of the tree.
_MIN_FILES_FOR_FEATURE = 1

# Hard cap so a giant monorepo does not produce hundreds of seed tasks.
_MAX_FEATURES = 12


def _slug_id(prefix: str, index: int) -> str:
    return f"{prefix}{index:03d}"


def _humanize(component: str) -> str:
    if component == "(root)":
        return "Repository root"
    return component.replace("-", " ").replace("_", " ").replace("/", " ").strip()


def draft_prd_from_model(
    model: CodebaseModel,
    *,
    project_name: str,
) -> str:
    """Return draft PRD markdown describing *model* with a seed feature/task graph.

    The PRD always contains a ``## Features`` and ``## Tasks`` section with at
    least one feature and one task (even an empty repo yields a single
    "establish project structure" task) so the downstream seed pipeline always
    has something to promote to ``ready``.
    """
    components = model.components()
    languages = model.language_counts()

    # Pick the components worth a feature: those with enough files, biggest
    # first, capped. Root-level files are always included if present.
    ranked = sorted(
        components.items(),
        key=lambda kv: (-len(kv[1]), kv[0]),
    )
    selected = [
        (name, files)
        for name, files in ranked
        if len(files) >= _MIN_FILES_FOR_FEATURE
    ][:_MAX_FEATURES]

    lang_summary = (
        ", ".join(f"{lang} ({count})" for lang, count in languages.items())
        or "no recognised source files"
    )

    lines: list[str] = []
    lines.append(f"# Project: {project_name}")
    lines.append("")
    lines.append("## Summary")
    lines.append("")
    lines.append(
        f"Draft PRD generated by `anvil scan` from the existing "
        f"working tree. The repository currently contains "
        f"{model.file_count} tracked source file(s) across "
        f"{len(components)} top-level component(s). Languages: {lang_summary}. "
        "This is a SEED — edit the goals, features, and tasks below to capture "
        "the real product intent before approving."
    )
    lines.append("")
    lines.append("## Goals")
    lines.append("")
    lines.append("- Document the intended behaviour of the existing codebase.")
    lines.append(
        "- Establish a claimable task graph so further work is coordinated."
    )
    lines.append("")
    lines.append("## Non-Goals")
    lines.append("")
    lines.append(
        "- This generated draft does not infer product requirements; a human "
        "must replace the placeholders below."
    )
    lines.append("")
    lines.append("## Requirements")
    lines.append("")
    # One requirement per selected component (so the PRD has structure the
    # review/decisions tooling can hang off).
    req_ids: list[str] = []
    for idx, (name, _files) in enumerate(selected, start=1):
        rid = _slug_id("R", idx)
        req_ids.append(rid)
        lines.append(
            f"- {rid}: Document and verify the `{name}` component "
            f"({_humanize(name)})."
        )
    if not req_ids:
        lines.append(
            "- R001: Establish the initial project structure and conventions."
        )
        req_ids.append("R001")
    lines.append("")
    lines.append("## Acceptance Criteria")
    lines.append("")
    lines.append(
        "- Every top-level component has a documented purpose and at least one "
        "verification step."
    )
    lines.append("")
    lines.append("## Open Questions")
    lines.append("")
    lines.append(
        "- What is the product intent behind each component? (Replace the "
        "generated placeholders.)"
    )
    lines.append("")

    # --- Features --------------------------------------------------------
    lines.append("## Features")
    lines.append("")
    feature_specs: list[tuple[str, str, str]] = []  # (feat_id, component, req_id)
    if selected:
        for idx, (name, files) in enumerate(selected, start=1):
            fid = _slug_id("F", idx)
            rid = req_ids[idx - 1] if idx - 1 < len(req_ids) else req_ids[0]
            feature_specs.append((fid, name, rid))
            lines.append(f"### {fid}: {_humanize(name)}")
            lines.append("")
            lines.append(
                f"The `{name}` component ({len(files)} file(s)). "
                "Describe what this part of the system does."
            )
            lines.append("")
            lines.append(f"**Requirements:** {rid}")
            lines.append("")
    else:
        feature_specs.append(("F001", "(root)", req_ids[0]))
        lines.append("### F001: Project structure")
        lines.append("")
        lines.append(
            "Establish the initial project structure for an otherwise empty "
            "repository."
        )
        lines.append("")
        lines.append(f"**Requirements:** {req_ids[0]}")
        lines.append("")

    # --- Tasks -----------------------------------------------------------
    lines.append("## Tasks")
    lines.append("")
    for idx, (fid, name, _rid) in enumerate(feature_specs, start=1):
        tid = _slug_id("T", idx)
        files = components.get(name, [])
        # Up to 5 representative likely_files so the task is anchored to real
        # paths (and dependency/conflict inference has something to work with).
        likely = ", ".join(f.path for f in files[:5]) if files else name
        lines.append(f"### {tid}: Document and verify {_humanize(name)}")
        lines.append("")
        lines.append(f"**Feature:** {fid}")
        lines.append("**Priority:** medium")
        if likely:
            lines.append(f"**Likely files:** {likely}")
        lines.append("")
        lines.append(
            f"Review the `{name}` component, document its behaviour, and add "
            "or confirm tests covering it. Replace this generated description "
            "with the real intent."
        )
        lines.append("")
        lines.append("**Acceptance criteria:**")
        lines.append("")
        lines.append(
            f"- The purpose of `{name}` is documented in the PRD or its README."
        )
        lines.append("- At least one automated check covers the component.")
        lines.append("")
        lines.append("**Verification:**")
        lines.append("")
        lines.append("- Run the project's test suite and confirm it passes.")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"
